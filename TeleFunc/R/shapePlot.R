
shapePlot<-function(x, y, shapeX=NULL,shapeY=NULL,ang=0,exp=1,...){
  op<-par()
  xdif<-op$usr[2]-par()$usr[1]
  ydif<-op$usr[4]-par()$usr[3]
  
  windasp<-op$pin[1]/op$pin[2]
  
  
  ang<-(ang/180)*pi
  xy<-Zb(ang) %*% rbind(shapeX,shapeY,rep(0,length(x)))
  
  xlocs<-(xy[1,]*exp*(xdif/2)) + x
  ylocs<-(xy[2,]*exp*windasp*(ydif/2)) + y
  
  polygon(x=xlocs,y=ylocs,...)  
  
}


BendFigureZ<-function(X,Y,Z,ang){
  rots<-rep(0,length(X))
  rots[which(X<0)]<-scal(x = X[which(X<0)],scal = c(ang,ang/5),lims=c(-1,0),limit=FALSE)
  rots[which(X>0)]<-scal(x = X[which(X>0)],scal = c(ang/5,ang/4),lims=c(0,1),limit=FALSE)
  out<-cbind(X,Y,Z)
  for(j in 1:length(X)){
    out[j,]<-c(X[j],Y[j],Z[j]) %*% XYZb(0,0,rots[j])
  }
  return(out)
}


